"""
A utility to generate the uplift schema code for the structured models for the current registered versions.
When a structured model has been updated, the dataset record version should be incremented and this utility run.
Then the changed dataset will need appropriate uplift functions for the changed models.
"""
import argparse
import importlib
import inspect
import json
import os

from dsp.dam import current_record_version
from dsp.datasets.definitions import ALL_TABLE_PREFIXES
from dsp.datasets.schema.helpers.destructure import Destructurer
from dsp.shared.constants import DS


def generate_schemas_for_current_version(dataset_id):
    root_folder = os.path.dirname(os.path.realpath(__file__))

    root_model = Destructurer.ROOT_MODELS.get(dataset_id)

    if not root_model:
        print(f"no models recorded in destructurer for {dataset_id}")
        try:
            model_module = importlib.import_module(f'dsp.datasets.{dataset_id}.schema.model')
        except ModuleNotFoundError:
            print(f"no model found at convention path for {dataset_id}")
            return
        if model_module and getattr(model_module, 'get_root_model', None) and model_module.get_root_model():
            print(f"found model at convention path for {dataset_id}")
            root_model = model_module.get_root_model()
        else:
            print(f"no model found at convention path for {dataset_id}")
            return

    latest_version = current_record_version(dataset_id)

    all_models = [root_model] + Destructurer.GET_ANONYMOUS_MODELS.get(dataset_id, lambda: [])()

    fn_name = inspect.currentframe().f_code.co_name

    for model in all_models:
        print(model.__table__)

        prefixes = ALL_TABLE_PREFIXES.get(dataset_id)

        if not prefixes:
            raise ValueError('No prefixes are defined for dataset {}'.format(dataset_id))

        table_prefix = prefixes.get(model.__table__).lower()
        table_folder = os.path.join(root_folder, dataset_id, table_prefix)
        version_filename = os.path.join(table_folder, 'version_{}.py'.format(latest_version))
        os.makedirs(table_folder, exist_ok=True)

        print("Creating {}".format(version_filename))
        with open(version_filename, 'w+', encoding='utf8') as version_file:
            version_file.write('"""\n\n')
            version_file.write(
                'THIS FILE IS GENERATED BY {} - DO NOT MANUALLY EDIT\n\n'.format(fn_name))
            version_file.write('Schema for record version {} of {}\n\n'.format(latest_version, model.__name__))
            version_file.write('"""\n')
            version_file.write('from pyspark.sql.types import StructType\n\n')
            version_file.write('schema_json = ')
            version_file.write(
                json.dumps(model.get_struct().jsonValue(), indent=4).
                    replace(': true', ': True').
                    replace(': false', ': False')
            )
            version_file.write('\n\n')
            version_file.write('schema = StructType.fromJson(schema_json)\n')


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument('dataset', type=str, help='dataset id', default=None)

    arguments = parser.parse_args()
    dataset = arguments.dataset

    if dataset:
        if dataset not in vars(DS).values():
            raise ValueError(f"{dataset} is not a valid dataset.")

        generate_schemas_for_current_version(dataset)
    else:
        for ds in {v for k, v in vars(DS).items() if not k.startswith('__') and type(v) == str}:
            print(ds)
            generate_schemas_for_current_version(ds)


if __name__ == '__main__':
    main()
